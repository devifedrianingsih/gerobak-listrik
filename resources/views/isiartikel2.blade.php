            <p>TANGERANG, KOMPAS.com - Gerobak Listrik (Gelis) ikut memeriahkan Gaikindo Indonesia International Auto Show (GIIAS) 2024 dengan membawa produk terbaru Gerod4 Overland. Deputy Marketing Director Gelis, Yan Eriza, mengatakan, hadirnya Gerod4 Overland merupakan komitmen Gelis untuk merevolusi mobilitas perkotaan dengan kendaraan listrik inovatif. Hening dan Bertenaga “Gerod4 adalah contoh komitmen kami terhadap keberlanjutan dan inovasi. Kami sangat senang bisa menampilkan model-model ini di GIIAS 2024 dan terus mendorong masa depan transportasi listrik," kata Yan dalam keterangan resmi, Minggu (28/7/2024).</p>
            <img src="{{ asset('img/artikel2.png') }}" alt="Gambar Gerobak Listrik" class="featured-image">
            <p>Gerod4 Overland mengunakan basis Gerod4 yang dimodifikasi dengan tampilan off road. Mobil listrik ini dirancang untuk menghadapi medan yang sulit sekalipun sambil memberikan performa yang luar biasa. Kisah Panji dan Pengakuan UNESCO Artikel Kompas.id Baknya belakang memungkinkan untuk mengangkut muatan hingga setengah ton, menjadikannya unggulan di pasar kendaraan listrik. Baca juga: Awas Macet, Ada Perbaikan Tol Jakarta-Tangerang sampai Minggu Depan Gerod4 memiliki dimensi rangka panjang 2.100 mm, lebar 1.250 mm dan tinggi 1.760 mm. Dimensi boks panjang 1.700 mm, lebar 1.250 mm dan tinggi 1.100 mm, dan volume boks 2.337 liter.</p>
            <p>"Hal ini menjadi bukti konkret atas kepercayaan yang diberikan Masjid Istiqlal terhadap kendaraan roda empat buatan Gelis," kata Eriza. Gerod4 Garbage didesain khusus dengan bak sampah dua bagian untuk memilah sampah organik dan sampah non-organik sehingga pengelolaan sampah akan lebih tepat sasaran. Gerod4 Garbage memiliki kemampuan daya angkut hingga 500 Kg, dan penggunaan motor penggerak 3000 Watt serta baterai LifePo4 menjadikan unit ini tidak hanya ramah lingkungan tetapi juga efisien dalam penggunaan energi.</p>
